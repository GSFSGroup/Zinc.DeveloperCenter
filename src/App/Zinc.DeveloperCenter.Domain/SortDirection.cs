namespace Zinc.DeveloperCenter.Domain
{
    /// <summary>
    /// An enum that defines a sort direction.
    /// </summary>
    public enum SortDirection
    {
        /// <summary>
        /// The default sort dirdction, ascending.
        /// </summary>
        Default,

        /// <summary>
        /// Sort ascending.
        /// </summary>
        Asc = 1,

        /// <summary>
        /// Sort descending.
        /// </summary>
        Desc = -1,
    }
}
